public class math {
    public static void main(String[] args) {
       double pi=Math.PI;
       System.out.println("the value of pi is "+pi);
       double e=Math.E;
       System.out.println("the value of E is "+e);
       double po=Math.pow(5, 3);
       System.out.println("the cube of 5 is "+po);
       double abosoute=Math.abs(-9);
       System.out.println("the absolute value of -9 "+abosoute);
       double root=Math.sqrt(abosoute);
       System.out.println("the sqrt of "+abosoute+" is "+root);
       double roun=Math.round(3.51);
       System.out.println("round to the nearest integer "+roun);
       double ceilresult=Math.ceil(3.10);
       System.out.println("take the ceil value of double or float "+ceilresult);
       double floor_result=Math.floor(5.9);
       System.out.println("takes the floor value of given decimal number "+floor_result);
       double max_number=Math.max(20, 10);
       System.out.println("max of two number "+max_number);
       double min_number=Math.min(20, 10);
       System.out.println("min of two number "+min_number);
    }
}
